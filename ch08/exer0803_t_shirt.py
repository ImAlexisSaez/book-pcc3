def make_shirt(size, message):
    """Informa del tamaño y el mensaje a imprimir."""
    print(f"\nLa camiseta es de tamaño {size}.")
    print(f"El mensaje a imprimir es: '{message}'.")

make_shirt('S', 'Hola Python!')
make_shirt(size='M', message='Me abuuuuuurro')
make_shirt(message='2+2=5', size='XXL')
