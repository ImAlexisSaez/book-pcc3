# Ejemplo de uso de variables y operaciones aritméticas con comentarios

# Datos
dato_1 = 10
dato_2 = 15

# Operaciones
operacion_1 = dato_1 + dato_2
operacion_2 = dato_1 - dato_2
operacion_3 = dato_1 * dato_2
operacion_4 = dato_1 / dato_2

# Muestra el resultado de la operación por pantalla
print(operacion_1)
print(operacion_2)
print(operacion_3)
print(operacion_4)